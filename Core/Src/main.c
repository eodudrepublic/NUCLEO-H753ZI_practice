{
  HAL_UART_Transmit(&huart3, (uint8_t *)str, strlen(str), HAL_MAX_DELAY);
}

/* return led name */
const char* LED_Name(Led_TypeDef led)
{
  if (led == LED_GREEN)
  {
    return "GREEN LED";
  }
  else if (led == LED_BLUE)
  {
    return "BLUE LED";
  }
  else if (led == LED_RED)
  {
    return "RED LED";
  }
  else
  {
    return "UNKNOWN LED";
  }
}
